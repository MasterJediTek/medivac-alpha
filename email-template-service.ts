class EmailTemplateService {
  private templates: EmailTemplate[] = [];
  private versions: TemplateVersion[] = [];
  private usageRecords: TemplateUsageRecord[] = [];

  async initialize(): Promise<void> {
    await this.loadTemplates();
    await this.loadVersions();
    await this.loadUsageRecords();

    // Create default templates if none exist
    if (this.templates.length === 0) {
      await this.createDefaultTemplates();
    }
  }

  private async loadTemplates(): Promise<void> {
    try {
      const data = await AsyncStorage.getItem(STORAGE_KEYS.TEMPLATES);
      if (data) {
        this.templates = JSON.parse(data);
      }
    } catch (error) {
      console.error('Failed to load email templates:', error);
    }
  }

  private async loadVersions(): Promise<void> {
    try {
      const data = await AsyncStorage.getItem(STORAGE_KEYS.TEMPLATE_VERSIONS);
      if (data) {
        this.versions = JSON.parse(data);
      }
    } catch (error) {
      console.error('Failed to load template versions:', error);
    }
  }

  private async loadUsageRecords(): Promise<void> {
    try {
      const data = await AsyncStorage.getItem(STORAGE_KEYS.TEMPLATE_USAGE);
      if (data) {
        this.usageRecords = JSON.parse(data);
      }
    } catch (error) {
      console.error('Failed to load template usage records:', error);
    }
  }

  private async saveTemplates(): Promise<void> {
    await AsyncStorage.setItem(STORAGE_KEYS.TEMPLATES, JSON.stringify(this.templates));
  }

  private async saveVersions(): Promise<void> {
    await AsyncStorage.setItem(STORAGE_KEYS.TEMPLATE_VERSIONS, JSON.stringify(this.versions));
  }

  private async saveUsageRecords(): Promise<void> {
    await AsyncStorage.setItem(STORAGE_KEYS.TEMPLATE_USAGE, JSON.stringify(this.usageRecords));
  }

  private async createDefaultTemplates(): Promise<void> {
    for (const template of DEFAULT_TEMPLATES) {
      await this.createTemplate(template);
    }
  }

  /**
   * Get all templates
   */
  getTemplates(): EmailTemplate[] {
    return [...this.templates];
  }

  /**
   * Get templates by category
   */
  getTemplatesByCategory(category: TemplateCategory): EmailTemplate[] {
    return this.templates.filter(t => t.category === category);
  }

  /**
   * Get template by ID
   */
  getTemplate(id: string): EmailTemplate | null {
    return this.templates.find(t => t.id === id) || null;
  }

  /**
   * Create new template
   */
  async createTemplate(template: Partial<EmailTemplate>): Promise<EmailTemplate> {
    const now = new Date().toISOString();
    const newTemplate: EmailTemplate = {
      id: `template_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
      name: template.name || 'New Template',
      description: template.description || '',
      category: template.category || 'general',
      subject: template.subject || '',
      bodyHtml: template.bodyHtml || '',
      bodyText: template.bodyText || '',
      variables: template.variables || [],
      status: template.status || 'draft',
      version: 1,
      tags: template.tags || [],
      createdAt: now,
      updatedAt: now,
      createdBy: template.createdBy || 'system',
      usageCount: 0,
    };

    this.templates.push(newTemplate);
    await this.saveTemplates();

    // Create initial version
    await this.createVersion(newTemplate.id, 'Initial version');

    return newTemplate;
  }

  /**
   * Update template
   */
  async updateTemplate(id: string, updates: Partial<EmailTemplate>, changeNotes?: string): Promise<EmailTemplate> {
    const index = this.templates.findIndex(t => t.id === id);
    if (index < 0) {
      throw new Error('Template not found');
    }

    const template = this.templates[index];
    const hasContentChanges = 
      updates.subject !== undefined && updates.subject !== template.subject ||
      updates.bodyHtml !== undefined && updates.bodyHtml !== template.bodyHtml ||
      updates.bodyText !== undefined && updates.bodyText !== template.bodyText ||
      updates.variables !== undefined;

    this.templates[index] = {
      ...template,
      ...updates,
      version: hasContentChanges ? template.version + 1 : template.version,
      updatedAt: new Date().toISOString(),
    };

    await this.saveTemplates();

    // Create new version if content changed
    if (hasContentChanges) {
      await this.createVersion(id, changeNotes || 'Updated template');
    }

    return this.templates[index];
  }

  /**
   * Delete template
   */
  async deleteTemplate(id: string): Promise<boolean> {
    const index = this.templates.findIndex(t => t.id === id);
    if (index < 0) {
      return false;
    }

    this.templates.splice(index, 1);
    await this.saveTemplates();

    // Also delete versions
    this.versions = this.versions.filter(v => v.templateId !== id);
    await this.saveVersions();

    return true;
  }

  /**
   * Create template version
   */
  private async createVersion(templateId: string, changeNotes: string): Promise<TemplateVersion> {
    const template = this.getTemplate(templateId);
    if (!template) {
      throw new Error('Template not found');
    }

    const version: TemplateVersion = {
      id: `version_${Date.now()}`,
      templateId,
      version: template.version,
      subject: template.subject,
      bodyHtml: template.bodyHtml,
      bodyText: template.bodyText,
      variables: [...template.variables],
      createdAt: new Date().toISOString(),
      createdBy: 'system',
      changeNotes,
    };

    this.versions.push(version);
    await this.saveVersions();

    return version;
  }

  /**
   * Get template versions
   */
  getTemplateVersions(templateId: string): TemplateVersion[] {
    return this.versions
      .filter(v => v.templateId === templateId)
      .sort((a, b) => b.version - a.version);
  }

  /**
   * Restore template to specific version
   */
  async restoreVersion(templateId: string, versionId: string): Promise<EmailTemplate> {
    const version = this.versions.find(v => v.id === versionId && v.templateId === templateId);
    if (!version) {
      throw new Error('Version not found');
    }

    return this.updateTemplate(templateId, {
      subject: version.subject,
      bodyHtml: version.bodyHtml,
      bodyText: version.bodyText,
      variables: version.variables,
    }, `Restored to version ${version.version}`);
  }

  /**
   * Preview template with variables
   */
  previewTemplate(templateId: string, variableValues: Record<string, string>): TemplatePreview {
    const template = this.getTemplate(templateId);
    if (!template) {
      throw new Error('Template not found');
    }

    const missingVariables: string[] = [];
    let subject = template.subject;
    let bodyHtml = template.bodyHtml;
    let bodyText = template.bodyText;

    // Replace variables
    for (const variable of template.variables) {
      const value = variableValues[variable.name] || variable.defaultValue;
      const placeholder = `{{${variable.name}}}`;

      if (variable.required && !variableValues[variable.name] && !variable.defaultValue) {
        missingVariables.push(variable.name);
      }

      subject = subject.replace(new RegExp(placeholder, 'g'), value);
      bodyHtml = bodyHtml.replace(new RegExp(placeholder, 'g'), value);
      bodyText = bodyText.replace(new RegExp(placeholder, 'g'), value);
    }

    return {
      subject,
      bodyHtml,
      bodyText,
      missingVariables,
    };
  }

  /**
   * Record template usage
   */
  async recordUsage(templateId: string, recipientCount: number, success: boolean, errorMessage?: string): Promise<void> {
    const record: TemplateUsageRecord = {
      id: `usage_${Date.now()}`,
      templateId,
      usedAt: new Date().toISOString(),
      usedBy: 'system',
      recipientCount,
      success,
      errorMessage,
    };

    this.usageRecords.push(record);
    await this.saveUsageRecords();

    // Update template usage count
    const template = this.getTemplate(templateId);
    if (template) {
      await this.updateTemplate(templateId, {
        usageCount: template.usageCount + 1,
        lastUsedAt: record.usedAt,
      });
    }
  }

  /**
   * Get template usage statistics
   */
  getUsageStats(templateId?: string): {
    totalUsage: number;
    successRate: number;
    averageRecipients: number;
    usageByCategory: Record<TemplateCategory, number>;
  } {
    const records = templateId 
      ? this.usageRecords.filter(r => r.templateId === templateId)
      : this.usageRecords;

    const totalUsage = records.length;
    const successCount = records.filter(r => r.success).length;
    const totalRecipients = records.reduce((sum, r) => sum + r.recipientCount, 0);

    const usageByCategory: Record<TemplateCategory, number> = {
      compliance: 0,
      incident: 0,
      policy: 0,
      general: 0,
      clinical: 0,
      administrative: 0,
    };

    for (const record of records) {
      const template = this.getTemplate(record.templateId);
      if (template) {
        usageByCategory[template.category]++;
      }
    }

    return {
      totalUsage,
      successRate: totalUsage > 0 ? (successCount / totalUsage) * 100 : 0,
      averageRecipients: totalUsage > 0 ? totalRecipients / totalUsage : 0,
      usageByCategory,
    };
  }

  /**
   * Duplicate template
   */
  async duplicateTemplate(id: string, newName?: string): Promise<EmailTemplate> {
    const template = this.getTemplate(id);
    if (!template) {
      throw new Error('Template not found');
    }

    return this.createTemplate({
      ...template,
      name: newName || `${template.name} (Copy)`,
      status: 'draft',
    });
  }

  /**
   * Export templates
   */
  exportTemplates(ids?: string[]): string {
    const templates = ids 
      ? this.templates.filter(t => ids.includes(t.id))
      : this.templates;

    return JSON.stringify(templates, null, 2);
  }

  /**
   * Import templates
   */
  async importTemplates(jsonData: string): Promise<{ imported: number; errors: string[] }> {
    const errors: string[] = [];
    let imported = 0;

    try {
      const templates = JSON.parse(jsonData) as Partial<EmailTemplate>[];
      
      for (const template of templates) {
        try {
          // Remove ID to create new template
          const { id, ...templateData } = template;
          await this.createTemplate(templateData);
          imported++;
        } catch (error) {
          errors.push(`Failed to import "${template.name}": ${error}`);
        }
      }
    } catch (error) {
      errors.push(`Invalid JSON format: ${error}`);
    }

    return { imported, errors };
  }

  /**
   * Search templates
   */
  searchTemplates(query: string): EmailTemplate[] {
    const lowerQuery = query.toLowerCase();
    return this.templates.filter(t => 
      t.name.toLowerCase().includes(lowerQuery) ||
      t.description.toLowerCase().includes(lowerQuery) ||
      t.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
    );
  }
}

export const emailTemplateService = new EmailTemplateService();
