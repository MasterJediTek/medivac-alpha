/**
 * Email Template Service
 * Customizable templates for compliance reports, incident alerts, and policy notifications
 * MediVac One v5.5
 */

import AsyncStorage from '@react-native-async-storage/async-storage';

// Storage keys
const STORAGE_KEYS = {
  TEMPLATES: 'medivac_email_templates',
  TEMPLATE_VERSIONS: 'medivac_template_versions',
  TEMPLATE_USAGE: 'medivac_template_usage',
};

// Types
export type TemplateCategory = 'compliance' | 'incident' | 'policy' | 'general' | 'clinical' | 'administrative';
export type TemplateStatus = 'draft' | 'active' | 'archived';

export interface TemplateVariable {
  name: string;
  description: string;
  defaultValue: string;
  required: boolean;
  type: 'text' | 'number' | 'date' | 'list' | 'boolean';
}

export interface EmailTemplate {
  id: string;
  name: string;
  description: string;
  category: TemplateCategory;
  subject: string;
  bodyHtml: string;
  bodyText: string;
  variables: TemplateVariable[];
  status: TemplateStatus;
  version: number;
  tags: string[];
  createdAt: string;
  updatedAt: string;
  createdBy: string;
  lastUsedAt?: string;
  usageCount: number;
}

export interface TemplateVersion {
  id: string;
  templateId: string;
  version: number;
  subject: string;
  bodyHtml: string;
  bodyText: string;
  variables: TemplateVariable[];
  createdAt: string;
  createdBy: string;
  changeNotes: string;
}

export interface TemplatePreview {
  subject: string;
  bodyHtml: string;
  bodyText: string;
  missingVariables: string[];
}

export interface TemplateUsageRecord {
  id: string;
  templateId: string;
  usedAt: string;
  usedBy: string;
  recipientCount: number;
  success: boolean;
  errorMessage?: string;
}

